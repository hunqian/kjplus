package com.ybk.bean;

public class ParamVl {
	private String pm;
	private String vl;
	
	public ParamVl(){}
	public ParamVl(String pm, String vl) {
		super();
		this.pm = pm;
		this.vl = vl;
	}

	public String getPm() {
		return pm;
	}

	public void setPm(String pm) {
		this.pm = pm;
	}

	public String getVl() {
		return vl;
	}

	public void setVl(String vl) {
		this.vl = vl;
	}

}
